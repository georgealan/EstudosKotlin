package com.kotlin.apprentice.kotlinbasics.operacoesaritmeticas

fun main() {

    // Incremento e decremento
    var contador: Int = 0

    contador += 1
    println("Incremento de 1: $contador")

    contador -= 1
    println("Decremento de 1: $contador")

    // Incremento com outros operadores matematicos
    var contador2: Int = 10

    contador2 *= 3
    println("Incremento de multiplicação: $contador2")

    contador2 /= 2
    println("Incremento de divisão: $contador2")

}