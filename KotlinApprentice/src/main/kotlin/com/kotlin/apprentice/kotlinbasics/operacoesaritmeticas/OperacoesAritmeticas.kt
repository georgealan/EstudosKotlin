package com.kotlin.apprentice.kotlinbasics.operacoesaritmeticas

fun main() {

    // Simples operações aritméticas
    println("Simples operações aritméticas")
    println(5 + 5)
    println(10 - 5)
    println(2 * 5)
    println(10 / 5)
    print("\n")

    // Removendo espaços, que são válidos
    println("Removendo espaços, que são válidos")
    println(5+5)
    println(10 -5)
    println(2* 5)
    println(10/5)
    print("\n")

    // Números decimais
    println("Operações com números decimais")
    println(22.0 / 7.0)
    print("\n")

    // Resto de divisão
    println("Resto de divisão")
    println(10 % 3)
    println("%.0f".format(28.0 % 10.0))
    println("%.0f".format(10.0 % 3.0))
    print("\n")
}